CREATE TABLE requests_Sila (
    request_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    creator_ID NUMBER NOT NULL,
    title VARCHAR2(255) NOT NULL,
    description VARCHAR2(4000) NOT NULL,
    
    status VARCHAR2(20) DEFAULT 'PENDING' NOT NULL,
    final_priority_score NUMBER(10, 2), 
    
    CONSTRAINT chk_status CHECK (status IN ('APPROVED', 'PENDING', 'DONE', 'REJECTED', 'IN_PROGRESS')),
    CONSTRAINT fk_req_creator FOREIGN KEY (creator_ID) REFERENCES users_Sila(user_ID)
);

CREATE TABLE priorities_Sila(
    priority_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    request_ID NUMBER NOT NULL,
    company_points NUMBER (10,2),
    customer_rank NUMBER,
    product_mgr_score NUMBER,
    software_mgr_score NUMBER,
    payment_priority NUMBER,
    priority_score NUMBER (10,2),
    CONSTRAINT chk_product_score CHECK (product_mgr_score BETWEEN 1 AND 5),
    CONSTRAINT chk_software_score CHECK (software_mgr_score BETWEEN 1 AND 5),
    CONSTRAINT chk_request_ID FOREIGN KEY (request_ID) REFERENCES requests_Sila(request_ID)
);

CREATE TABLE workflows_Sila(
    task_ID NUMBER(8),
    request_ID NUMBER,
    developer_ID NUMBER,
    workflow_status VARCHAR2(20) DEFAULT 'UNASSIGNED' NOT NULL,

    CONSTRAINT fk_req_developer FOREIGN KEY (developer_ID) REFERENCES users_Sila(user_ID),
    CONSTRAINT fk_wf_request_id FOREIGN KEY (request_ID) REFERENCES requests_Sila(request_ID),
    CONSTRAINT chk_workflow_status CHECK (workflow_status IN ('ASSIGNED', 'UNASSIGNED', 'RESOLVED', 'RESENT'))
);

