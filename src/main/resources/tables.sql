CREATE TABLE users_Sila(
       user_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
       name_surname VARCHAR2(50) NOT NULL,
       email VARCHAR2(100) NOT NULL UNIQUE,
       role VARCHAR2(20) NOT NULL,
       password VARCHAR2(50) NOT NULL,
       company_ID NUMBER(8) NOT NULL,
       
       CONSTRAINT chk_user_role CHECK (role IN ('ADMIN','YAZILIMCI','MUSTERI','URUN_SORUMLUSU','YAZILIM_YONETICISI')),
       CONSTRAINT fk_company_ID FOREIGN KEY(company_ID) REFERENCES company_Sila(company_ID)
);

